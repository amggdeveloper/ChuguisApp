								<div class="entry-content">
									<p>
									Actualmente todos los bebés de la sociedad en la que vivimos utilizan algún tipo de pañal. La razón fundamental es la higiene,
									principalmente del bebé pero también del resto de la familia y la limpieza de la vivienda en sí. 
									La comodidad es otro motivo importante para su uso masivo. Todos cumplen, algunos mejor y otros no tan bien, estos requisitos,
									y hay quien no se preocupa por cuál elegir. Pero a algunos padres sí les surge la inquietud o la duda de qué tipo elegir.
									Actualmente el uso de pañales de tela se está apoyando desde distintos estamentos, y queremos explicar a aquellos padres que 
									se lo hayan planteado la mejor forma de comenzar a usarlos y sus beneficios frente a los desechables.
									</p> 

									<p> 
									Existen tres grandes grupos, que son: los desechables ordinarios, los desechables ecológicos y los de tela.
									Estos últimos tienen muchos beneficios que a continuación pasamos a citar:
									</p>

									<p>
									<strong>Ecología.</strong> Ni que decir tiene que el uso de la tela evita kilos de pañales desechables que 
									deterioran el medioambiente tanto en el momento de su producción, ya que se utilizan derivados del petróleo, como en su 
									descomposición, ya que tardan entre 300 y 400 años en desaparecer en la naturaleza. La media de generación de basura por el uso 
									de desechables por cada bebé es de unas 2 toneladas.
									</p>
									<p>
									<strong>Salud.</strong> Mientras que los desechables ordinarios tienen en su composición sustancias derivadas del petróleo 
									y muy contaminantes que no deberían estar en contacto con la piel del bebé, los de tela, y también los ecológicos, están 
									libres de dichas sustancias.
									</p>
									<p>
									<strong>Transpirables.</strong> Al estar los pañales de tela compuestos por fibras naturales que permiten el paso del aire 
									hacen que el culito del bebé esté más sano y fresco. La transpirabilidad es una de las características más destacadas.
									</p>
									<p>
									<strong>Economía.</strong> El coste del uso de los de tela durante toda la etapa del pañal del bebé frente a los desechables,
									de cualquier tipo, es mucho menor. Se realiza un inversión inicial y gracias a la posibilidad de lavarlos y volver a utilizarlos 
									no sería necesario comprarlos a menudo. Incluso pueden ser utilizados con un segundo y tercer bebé.
									</p>
									<p>
									<strong>Estilo.</strong> Gracias a su gran variedad de materiales, colores y diseños son un complemento más a la hora de vestir 
									al bebé, ya que hay para todos los gustos y se pueden combinar con cualquier otra prenda.
									</p>

									<p>Hay que tener en cuenta que no necesitamos la misma cantidad con bebés recién nacidos como con niños pequeños. Los bebés 
									pequeñitos ensucian mucha más veces pero con menos cantidad y los mayores ensuciarán menos veces al día pero necesitarán una 
									absorción mucho mayor. Por lo que en recién nacidos utilizarías absorbentes más pequeños pero hará más o menos unos 10 cambios 
									al día, en cambio para niños mayores utilizás absorbentes con mayor cantidad de absorción o más grandes ya que la cantidad de 
									liquido será mayor pero reducirás los cambios a más o menos 5. Por lo general con unos veinte deberían de ser suficientes para no 
									tener la necesidad de lavar en tres días. Esto no significa que recomendemos comprar 20 unidades desde el principio, sino que 
									pensamos que es preferible ir probando diferentes tipos, modelos y marcas, ya que algunos te gustarán más y le irán mejor al bebé 
									que otros, pudiendo elegir en función de las características y bondades de cada uno, como la capacidad de absorción, la economía, 
									el grosor, el diseño o el color...</p>

									<blockquote>
										<p>El proceso de lavado, en algunos casos desconocido y que por desconocimiento puede desanimar a algunos padres a su 
										utilización, es muy sencillo y no deja rastro en las prendas, ya que la suciedad es muy fácil de quitar.
										Guarda los pañales sucios en un cubo con tapa para evitar olores, es bueno pasar por un chorro de agua los que estén sucios 
										de deposiciones, al menos para quitar la parte más sucia, antes de dejarlos en el cubo. Otra opción es enjuagar cada uno 
										después del cambio y dejarlo en un sitio aireado para que se vaya secando antes de lavarlo. Cuando acumules suficientes unidades 
										(8-10) mételos en la lavadora con un jabón neutro y sin suavizante, ya que éste impermeabiliza las telas. Elige un lavado corto 
										a 30-40º sin prelavado y pon un aclarado extra para eliminar los restos de jabón y tus pañales saldrán limpísimos.
										El secado ideal sería al sol, que además acaba con los restos de manchas que puedan quedar, y de no ser así, simplemente tendidos.
										Aun así, los absorbentes se pueden meter en la secadora. Desaconsejamos meter los cobertores en la secadora, ya que acabarían 
										estropeándose. Existen alternativas a los jabones que generan residuos biodegradables como las nueces de lavado.</p>
									</blockquote>

									<p>Para explicar los tipos existentes en el mercado, tenemos que explicar previamente las partes principales en que 
									se dividen, que son: la parte absorbente y la impermeable. La primera absorbe la humedad evitando que estén en contacto 
									con el culito del bebé, mientras que la segunda evita que ésta pase a la ropita. La elección del tipo y marca depende 
									de los gustos y preferencias de los padres, la etapa del desarrollo del bebé, la facilidad de uso, la estructura corporal 
									del bebé entre otras. Los tipos fundamentales son:</p> 

									<p>
									<strong>Todo en uno: </strong>Son pañales de tela con absorbentes incluidos cosidos al cobertor de pul (poliuretano laminado) 
									impermeable. Es el más fácil de utilizar, perfecto para pasar de los desechables a los de tela y no cambiar de sistema ya que 
									se utiliza como un desechable, solo que en lugar de tirarlo, se lava. 
									</p>
									<p>
									<strong>Todo en dos: </strong>En este caso los absorbente están enganchados con "snaps", o corchetes, al cobertor impermeable,
									por lo que en muchas ocasiones se podría reutilizar el cobertor con absorbentes limpios sin necesidad de lavarlo durante varios 
									cambios.
									</p>
									<p>
									<strong>Todo Rellenables: </strong>Se caracterizan porque el cobertor tienen cosida una tela y se rellena con los absorbentes que se 
									necesiten utilizar en cada momento dependiendo de la absorción buscada, del tiempo de cambio, del grosor de la tela, etc. 
									</p>
									<p>
									<strong>Híbridos: </strong>Así llamamos a los que pueden utilizarse tanto con absorbentes de tela como con desechables. 
									</p>									
									<p>
									<strong>Todo en uno: </strong>Son pañales de tela con absorbentes incluidos cosidos al cobertor de pul (poliuretano laminado) 
									impermeable. Es el más fácil de utilizar, perfecto para pasar de los desechables a los de tela y no cambiar de sistema ya que 
									se utiliza como un desechable, solo que en lugar de tirarlo, se lava. 
									</p>
									<p>
									<strong>Gasas: </strong>Existen de diversos materiales, siendo las más habituales de algodón y las de bambú. Podemos encontrarlas de 
									algodón orgánico, algodón o bambú. Con ellas es posible confeccionar un pañal, pero también es posible utilizarlas como parte absorbente 
									de los 'rellenables' citados anteriormente.
									</p>								
