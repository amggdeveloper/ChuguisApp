									<h2>¿Pañales de Tela?</h2>
									<div class="entry-meta">
										<span class="entry-category"><i class="fa fa-folder"></i> <a href="entrada.php">Pañales</a></span>
										<span class="entry-author"><i class="fa fa-user"></i> <a href="http://amggdeveloper.pythonanywhere.com/" target='_blank'>amgg Developer</a></span>
